
package ipc12s2022examenfinal;

public class vaca {
    
    private int numero,peso,litros;

    public vaca(int numero, int peso, int litros) {
        this.numero = numero;
        this.peso = peso;
        this.litros = litros;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getLitros() {
        return litros;
    }

    public void setLitros(int litros) {
        this.litros = litros;
    }
    
    
    
}
